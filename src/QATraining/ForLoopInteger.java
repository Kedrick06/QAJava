package qatraining;

public class ForLoopInteger {
	
	public static void main(String[] args) {
		
	int[] array= new int[10];
	
	for (int x = 0; x < array.length; x++) {
		array[x] = x + 1;
		System.out.println(array[x] + " ");
	}
	
}
	}

	
